//----------------------------------------

struct Pair {
    float x;
    float y;

    Pair() : x(0), y(0) {
    }

    Pair(float ix, float iy) : x(ix), y(iy) {
    }
    
     Pair (std::initializer_list<float> l)
     {
         if(  l.size() != 2)
             throw "Pair does not have enough values";
                 
         const float *it  = l.begin();
         x = *it ;
         y = *(++it);
         
     }
     
};

//-------------COORDINATE STRUCT ------------
typedef struct Coordinate_item Coordinate_item;

struct Coordinate_item {
    float x;
    float y;
    int caption_id;
    struct Coordinate_item *nxt;

    Coordinate_item() : nxt(0) {
    }

    int count() {
        int nc = 0;
        ++nc;
        Coordinate_item* temp = nxt;
        while (temp != NULL) {
            ++nc;
            temp = temp->nxt;
        }

        return nc;
    }

    Pair min() {
        Pair nc(x, y);
        Coordinate_item* temp = nxt;
        while (temp != NULL) {

            if (nc.x > temp->x) {
                nc.x = temp->x;
            }
            if (nc.y > temp->y) {
                nc.y = temp->y;
            }

            temp = temp->nxt;
        }


        return nc;

    }

    Pair max() {
        Pair nc(x, y);
        Coordinate_item* temp = nxt;
        while (temp != NULL) {

            if (nc.x < temp->x) {
                nc.x = temp->x;
            }
            if (nc.y < temp->y) {
                nc.y = temp->y;
            }

            // ++ nc;
            temp = temp->nxt;
        }

        return nc;
    }


};

typedef Coordinate_item* coordlist;
